x = 100
y = "100.00"
print("The value of x is {z:.2f}.".format(z=x))
print("The value of x is {:s}.".format(y))
print("The value of x is {:.2e}.".format(x))
print()
print(f"The value of x is {x:.2f}.")
print(f"The value of x is {y:s}.")
print(f"The value of x is {x:.2e}.")
